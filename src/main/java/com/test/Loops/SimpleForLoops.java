package com.test.Loops;

public class SimpleForLoops {

	public static void main(String[] args) {
		

		// For Loop Syntax
		// for(initialization, condition, Increment/Decrement){}
		
			
		// 9 * 1 = 9
		// 9 * 2 = 18
		
		int num = 20;
		
		for(int i=1; i<=20; i++) {
			
			System.out.println(num + " * " + i  + " = " + (num * i));
		}
		
		

		
	}

}
