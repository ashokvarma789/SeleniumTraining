public class PatternD {

	public static void main(String[] args) {
		for (int i=0; i<=20;i++)	
		{
			
			if (i==7 ) {
				System.out.println();
			}
			else if	(i>=8 && i<=13 ) {
				System.out.println("*       *");	
			}
			else {
				System.out.print("*");
				
			}
			

	}
	}
}
