package com.test.sivamohand;

public class PatternF {

	public static void main(String[] args) {
		int i;
		
		for (i=0; i<=30; i++)
		{
			if (i>=10 && i<=15)
			{
				System.out.println("*");
			}
			else if (i>=25 && i<=30)
			{
				System.out.println("*");
			}
		
		else
		{
			System.out.print("*");
		}

	}

}
}